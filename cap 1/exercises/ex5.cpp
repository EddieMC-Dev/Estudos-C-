// Usando operador de inserção de stream
#include <iostream>

using std::cout;

int main()
{
    cout << "1 2 3 4\n"; //a)

    cout << "1 " << "2 " << "3 " << "4\n"; //b)

    cout << "1 ";
    cout << "2 ";
    cout << "3 ";
    cout << "4 "; //c)

    return 0;
}
